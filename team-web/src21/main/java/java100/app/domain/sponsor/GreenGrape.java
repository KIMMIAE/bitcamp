package java100.app.domain.sponsor;

import java.sql.Date;

public class GreenGrape {
    
    protected int greenGrapeNo;
    protected int memberNo;
    protected int chrCount;
    protected int chrAmount;
    protected String chrMethod;
    protected Date chrDate;
    
    public int getGreenGrapeNo() {
        return greenGrapeNo;
    }
    public void setGreenGrapeNo(int greenGrapeNo) {
        this.greenGrapeNo = greenGrapeNo;
    }
    public int getMemberNo() {
        return memberNo;
    }
    public void setMemberNo(int memberNo) {
        this.memberNo = memberNo;
    }
    public int getChrCount() {
        return chrCount;
    }
    public void setChrCount(int chrCount) {
        this.chrCount = chrCount;
    }
    public int getChrAmount() {
        return chrAmount;
    }
    public void setChrAmount(int chrAmount) {
        this.chrAmount = chrAmount;
    }
    public Date getChrDate() {
        return chrDate;
    }
    public void setChrDate(Date chrDate) {
        this.chrDate = chrDate;
    }
    public String getChrMethod() {
        return chrMethod;
    }
    public void setChrMethod(String chrMethod) {
        this.chrMethod = chrMethod;
    }
    
    

    
    
    
    
}










